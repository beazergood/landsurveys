<ul class="ul arrow-v2 large">
			
				<li><h5>Title (Boundary) Survey</h5> A survey map prepared by a licensed, registered surveyor.</li>
				<li><h5>Sight Plan Survey</h5> A survey preformed for the purpose of proposing construction on a property in order to obtain a building permit.</li>
				<li><h5>As-Built Survey</h5> Is usually required by municipalities to show a completed construction project before the municipality will issue a certificate of occupancy (C.O.) to the owner. This could be for a newly constructed home, or an existing house with a new addition, swimming pool, driveway, or other types of improvements. The as-built will show the actual completed construction, which will be reviewed and compared by the building department to the original proposed construction plans.
				</li>
				<li><h5>Topographical Survey</h5> Shows the elevation changes and other physical features such as rock crops, steep slopes, wet areas, etc.  to be used for subdivisions and engineering designs.
				</li>
				<li><h5>Wet Land Survey</h5> A survey preformed for the purpose of delineating the perimeter of a wet land to better understand how much of the property can be developed. Some wet land regulations mandate a buffer zone between the wet land and new construction. Often times, silt control filtration measures will be implemented to protect a wet land and therefore exact measurements must be taken.  
				</li>
				<li><h5>Lot Consolidation Survey</h5> Sometimes referred to as an "abandonment" survey. When a person owns or wants to purchase two or more contiguous properties for the purpose of combining them into one parcel. A surveyor will survey and map the parcels in question and delineate the internal lot lines that are to be abandoned. This map will be taken by the  owner or owners agent to the appropriate agencies, i.e.. Planning and zoning boards for their approval.
				</li>
				<li><h5>Subdivision Survey</h5> A survey performed for the purpose of dividing one parcel of land into two or more parcels. Most municipalities have two classes of subdivisions; major and minor.  Minor subdivisions are generally 2-3 lot splits depending on local codes and are much easier and less time consuming to achieve.  On the other hand, major subdivisions, creating numerous lots and new roads can be very time consuming. When consulting with a surveyor, the client should make his intentions clear from the onset as to what his expectations for development for his land are. A site visit should be coordinated with the surveyor for a preliminary feasibility study.  
				</li>
				</ul>
				<br/>
<p>Every town/city has it's own subdivision regulations that must be adhered to. In order for the subdivision map to become legally valid in most jurisdictions, a local governing body such as a planning board must review and approve the map. This agency will ensure that the plat conforms to all of the  local regulations. A survey preformed for the purpose of a subdivision may include a Topographic Survey, and or a Wetland Survey and / or a tree location survey as well as any other requirements listed in the subdivision regulations of that particular municipality.<br/>
					During the application process, submittals will be made, most commonly on a monthly basis to the local agency. The client and surveyor should consult after each meeting to discuss the planning boards comments and concerns. In this way, the client can better appreciate his options as he moves forward with the subdivision process.
				</p>
